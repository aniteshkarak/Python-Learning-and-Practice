

print("Hello\nWorld")   #Newline — moves the cursor to the next line.                                                                                                                                                                          
print("Name\tAge")  # Horizontal tab — adds a tab spacing
print("This is a backslash: \\")  # Backslash — escapes to produce a literal backslash.
print('It\'s good')   # Single quote — use inside single-quoted strings.           
print("He said, \"Hello\"")   #Double quote — use inside double-quoted strings.            
print("Hello\rWorld")  #Carriage return — moves cursor to the beginning of line (less used).
print("Hello\b!")      #Backspace — moves cursor one position back (rarely used).                  
print("Line1\fLine2")  #Form feed — page break control (very rarely used).           
print("Line3\vLine4")  #Vertical tab — another control character (rare).         
print("\x65")          #Character with hexadecimal value — two hex digits.                  
print("\N{GREEK SMALL LETTER PI}")  #Named Unicode character — uses the Unicode database name.
